            <h2 class="section-title section-title--center"><?php echo esc_html($title); ?></h2>
        <?php endif; ?>

        <div class="products-wrapper">

            <?php if (have_rows('products_display')) : ?>
                <?php while (have_rows('products_display')) : the_row(); 
                    $products = get_sub_field('product');
                    if ($products && is_array($products)) :
                        foreach ($products as $product) :
                            if (!$product) continue;

                            $product_id = $product->ID;
                            $product_obj = wc_get_product($product_id);
                            if (!$product_obj) continue;

                            $thumbnail = get_the_post_thumbnail_url($product_id, 'medium');
                            $price = wc_get_price_to_display($product_obj);
                            $categories = wp_get_post_terms($product_id, 'product_cat', ['fields' => 'names']);
                            $attributes = [];

                            if ($product_obj->get_attributes()) {
                                foreach ($product_obj->get_attributes() as $attribute) {
                                    $taxonomy = $attribute->get_name();
                                    $terms = wp_get_post_terms($product_id, $taxonomy, ['fields' => 'names']);
                                    $attributes = array_merge($attributes, $terms);
                                }
                            }
                ?>
                    <div class="product-box">
                        <div class="product-box__top">
                            <?php echo do_shortcode('[yith_wcwl_add_to_wishlist product_id="' . $product_id . '"]'); ?>
                            <a href="<?php echo get_permalink($product_id); ?>">
                                <img src="<?php echo esc_url($thumbnail); ?>" alt="">
                            </a>
                        </div>
                        <div class="product-box__bottom">
                            <div class="price-box">
                                <span class="price"><?php echo number_format($price, 0, ',', '.'); ?></span>
                                <span class="value">rsd</span>
                            </div>

                            <?php if (!empty($categories)) : ?>
                                <span class="product-cat"><?php echo implode(', ', $categories); ?></span>
                            <?php endif; ?>

                            <a href="<?php echo get_permalink($product_id); ?>" class="product-title-link">
                                <h3 class="product-title"><?php echo esc_html(get_the_title($product_id)); ?></h3>
                            </a>

                            <?php if (!empty($attributes)) : ?>
                                <span class="product-tax"><?php echo implode(', ', $attributes); ?></span>
                            <?php endif; ?>

                            <?php
                            $product = wc_get_product($product_id);
                            if ($product) :
                            ?>
                                <a href="<?php echo esc_url($product->add_to_cart_url()); ?>"
                                    data-quantity="1"
                                    class="add-to-cart-btn ajax_add_to_cart add_to_cart_button product_type_<?php echo esc_attr($product->get_type()); ?>"
                                    data-product_id="<?php echo esc_attr($product->get_id()); ?>"
                                    data-product_sku="<?php echo esc_attr($product->get_sku()); ?>"
                                    aria-label="<?php echo esc_attr($product->add_to_cart_description()); ?>"
                                    rel="nofollow">
                                    <span class="font-cart-plus"></span>
                                </a>
                            <?php endif; ?>
                        </div>
                    </div>

                <?php
                        endforeach;
                    endif;
                endwhile;
            endif;
            ?>
        </div>

